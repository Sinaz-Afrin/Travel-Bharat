/**
 * Find state by slug (URL-friendly format)
 * @param {string} slug - The URL slug (e.g., "andhra-pradesh", "jammu-and-kashmir")
 * @returns {object|undefined} State object or undefined
 */ 
export const getStateBySlug = (slug) => {
  if (!slug) return undefined;
  const normalizedSlug = slug.toLowerCase().trim();
  return statesData.find(s => s.slug === normalizedSlug);
};

/**
 * Find state by name (backward compatibility)
 * @deprecated Use getStateBySlug() instead for URL params
 * @param {string} stateName - The state name
 * @returns {object|undefined} State object or undefined
 */
export const getStateByName = (stateName) => {
  if (!stateName) return undefined;
  return statesData.find(
    s => s.id === stateName.toLowerCase().replace(/\s+/g, '-') || 
    s.state.toLowerCase() === stateName.toLowerCase()
  );
};
